const mongoose = require('mongoose')
var questionnaireSchema = new mongoose.Schema({
  title: {
    type: String,
    trim: true,
    required: true
  },
  description: {
    type: String,
    trim: true,
    required: true
  },
  updatedAt: {
    type: Date,
    default: Date.now
  },
  createdAt: {
    type: Date,
    default: Date.now
  }
});

const questionnaire = new mongoose.model('questionnaire', questionnaireSchema)
module.exports = questionnaire
